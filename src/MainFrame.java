    private void customer_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_customer_tableMouseClicked
        // 테이블 값 클릭 시 띄워주기!!
        String id = customer_t.getValueAt(customer_table.getSelectedRow(), 0).toString();
        String pw = null;
        String name = customer_t.getValueAt(customer_table.getSelectedRow(), 1).toString();
        String hintQ = null;
        String hintA = null;
        String email = null;
        String vip = null;
        
        try{
            DBM.dbOpen();
            //JOptionPane.showMessageDialog(null, "DB연결");
            String sql = "Select * from customer where cutm_id ='";
            sql += id;
            sql += "';";
            System.out.println(sql);
            DBM.DB_rs = DBM.DB_stmt.executeQuery(sql);        
            while(DBM.DB_rs.next()) {
                pw = DBM.DB_rs.getString(2).toString();
                hintQ = DBM.DB_rs.getString(4).toString();
                hintA = DBM.DB_rs.getString(5).toString();
                email = DBM.DB_rs.getString(6).toString();
                vip = DBM.DB_rs.getString(7).toString();
                
                mng_Customer_lblID.setText(id);
                mng_Customer_lblName.setText(name);
                System.out.println(pw + "\t" + hintQ + "\t" + hintA + "\t" + email+ "\t" + vip);
                mng_Customer_lblPW.setText(pw);
                mng_Customer_lblHintQ.setText(hintQ);
                mng_Customer_lblHintA.setText(hintA);
                mng_Customer_lblEmail.setText(email);
                if(vip.equals("1")) {
                    vip = "○";
                } else {
                    vip = "X";
                }
                mng_Customer_lblVip.setText(vip);
                DBM.dbClose();
                //JOptionPane.showMessageDialog(null, "DB닫음");
                break;
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "SQLException : " + e.getMessage());
            System.out.println("SQLException : " + e.getMessage());
        }
    }//GEN-LAST:event_customer_tableMouseClicked

