    private void mng_btnStockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mng_btnStockActionPerformed
        stock_t.setNumRows(0);
        //재고 DB 로드
        try{
            DBM.dbOpen();
            //JOptionPane.showMessageDialog(null, "DB연결");
            DBM.DB_rs = DBM.DB_stmt.executeQuery("Select * from stock;");
            String id=null;
            String name = null;
            String max = null;
            String current = null;
            String price = null;
            while(DBM.DB_rs.next()){               
                id = DBM.DB_rs.getString(1).toString();
                name = DBM.DB_rs.getString(2).toString();
                max = DBM.DB_rs.getString(3).toString();
                current = DBM.DB_rs.getString(4).toString();
                price = DBM.DB_rs.getString(5).toString();
                
                stock_t.insertRow(stock_t.getRowCount(), new Object[]{id,name,max,current,price});
            }
            DBM.dbClose();
            //JOptionPane.showMessageDialog(null, "DB닫음");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "SQLException : " + e.getMessage());
            System.out.println("SQLException : " + e.getMessage());
        }
        
        dialMngStock.setLocation(300,300);
        dialMngStock.setSize(410,500);
        dialMngStock.setVisible(true);
    }//GEN-LAST:event_mng_btnStockActionPerformed

